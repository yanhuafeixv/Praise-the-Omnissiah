#ifndef __zf_device_dl1x_hpp__
#define __zf_device_dl1x_hpp__

#include "zf_driver_file_string.hpp"
#include "zf_common_typedef.hpp"

// DL1X IIO框架 所有路径宏定义
#define DL1X_EVENT_PATH         "/sys/bus/iio/devices/iio:device2/events/in_voltage_change_en"
#define DL1X_DISTANCE_PATH      "/sys/bus/iio/devices/iio:device2/events/in_distance_raw"

// DL1X设备类型枚举 【核心修改：枚举名改为 dl1x_device_type_enum】
enum dl1x_device_type_enum {
    NO_FIND_DEVICE = 0,
    ZF_DEVICE_DL1A = 1,
    ZF_DEVICE_DL1B = 2,
};

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 DL1X测距传感器驱动类，继承字符串文件操作类，适配Linux IIO框架读取测距数据
// 参数说明 无
// 返回参数 无
// 使用示例 zf_device_dl1x dl1x_dev; dl1x_dev.init(); int16 dis = dl1x_dev.get_distance();
// 备注信息 向指定路径写1初始化→读该文件获取设备型号→打开距离文件→按需读取，无重复IO，性能最优
//-------------------------------------------------------------------------------------------------------------------
class zf_device_dl1x : public zf_driver_file_string
{
private:
    enum dl1x_device_type_enum  dl1x_dev_type;      // 【同步修改：枚举类型更新】
    int        fd_distance;             // 距离数据文件句柄

    // 私有化核心函数：通过文件句柄读取原始数据并转int16
    int16 dl1x_read_fd_data(int fd);
    // 私有化函数：关闭所有打开的文件句柄
    void dl1x_close_all_fd(void);

    // 禁用拷贝构造和赋值重载，规范约束
    zf_device_dl1x(const zf_device_dl1x&) = delete;
    zf_device_dl1x& operator=(const zf_device_dl1x&) = delete;

public:
//-------------------------------------------------------------------------------------------------------------------
// 函数简介 构造函数
// 参数说明 无
// 返回参数 无
// 使用示例 zf_device_dl1x dl1x_dev;
//-------------------------------------------------------------------------------------------------------------------
    zf_device_dl1x(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 析构函数
// 参数说明 无
// 返回参数 无
// 使用示例 自动调用
//-------------------------------------------------------------------------------------------------------------------
    ~zf_device_dl1x(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 获取当前识别到的DL1X设备类型
// 参数说明 无
// 返回参数 enum dl1x_device_type_enum 设备类型枚举值 【同步修改：返回值类型更新】
// 使用示例 enum dl1x_device_type_enum type = dl1x_dev.get_dev_type();
//-------------------------------------------------------------------------------------------------------------------
    enum dl1x_device_type_enum get_dev_type(void);  // 【同步修改：返回值类型更新】

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取测距传感器原始距离数据
// 参数说明 无
// 返回参数 int16 距离原始值，未初始化返回0
// 使用示例 int16 distance = dl1x_dev.get_distance();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_distance(void);
    
//-------------------------------------------------------------------------------------------------------------------
// 函数简介 DL1X设备初始化【核心流程：写1初始化→读该文件获型号→打开距离文件】
// 参数说明 无
// 返回参数 enum dl1x_device_type_enum 识别到的DL1X设备类型，值对应枚举定义 【同步修改：返回值类型更新】
// 使用示例 enum dl1x_device_type_enum type = dl1x_dev.init();
//-------------------------------------------------------------------------------------------------------------------
    enum dl1x_device_type_enum init(void);  // 【同步修改：返回值类型更新】

};

#endif