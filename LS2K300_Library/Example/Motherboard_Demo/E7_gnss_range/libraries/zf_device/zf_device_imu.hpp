#ifndef __zf_device_imu_hpp__
#define __zf_device_imu_hpp__

#include "zf_driver_file_string.hpp"
#include "zf_common_typedef.hpp"

//===================================================================================================================
// 1. IMU IIO框架 所有路径宏定义 【全部抽离，集中管理】
// 核心路径：初始化+读取型号 都用该文件，不再用name文件
#define IMU_EVENT_PATH          "/sys/bus/iio/devices/iio:device1/events/in_voltage_change_en"
// IIO框架传感器原始数据路径
#define IMU_ACC_X_PATH          "/sys/bus/iio/devices/iio:device1/in_accel_x_raw"
#define IMU_ACC_Y_PATH          "/sys/bus/iio/devices/iio:device1/in_accel_y_raw"
#define IMU_ACC_Z_PATH          "/sys/bus/iio/devices/iio:device1/in_accel_z_raw"
#define IMU_GYRO_X_PATH         "/sys/bus/iio/devices/iio:device1/in_anglvel_x_raw"
#define IMU_GYRO_Y_PATH         "/sys/bus/iio/devices/iio:device1/in_anglvel_y_raw"
#define IMU_GYRO_Z_PATH         "/sys/bus/iio/devices/iio:device1/in_anglvel_z_raw"
#define IMU_MAG_X_PATH          "/sys/bus/iio/devices/iio:device1/in_magn_x_raw"
#define IMU_MAG_Y_PATH          "/sys/bus/iio/devices/iio:device1/in_magn_y_raw"
#define IMU_MAG_Z_PATH          "/sys/bus/iio/devices/iio:device1/in_magn_z_raw"
//===================================================================================================================

//===================================================================================================================
// 2. IMU设备类型
// 枚举值严格对应原始数值 0/1/2/3/4，完全兼容原代码逻辑，类型安全
typedef enum
{
    DEV_NO_FIND    = 0,    // 未识别到设备
    DEV_IMU660RA   = 1,    // IMU660RA型号
    DEV_IMU660RB   = 2,    // IMU660RB型号
    DEV_IMU660RC   = 3,    // IMU660RC型号
    DEV_IMU963RA   = 4     // IMU963RA型号(带磁力计)
}imu_device_type_enum;
//===================================================================================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 IMU传感器驱动类，继承字符串文件操作类，适配Linux IIO框架读取IMU数据
// 参数说明 无
// 返回参数 无
// 使用示例 zf_device_imu imu_dev; imu_dev.init(); int16 ax = imu_dev.get_acc_x();
// 备注信息 向指定路径写1初始化→读该文件获取设备型号→按型号动态打开文件→按需读取，无重复IO，性能最优
//-------------------------------------------------------------------------------------------------------------------
class zf_device_imu : public zf_driver_file_string
{
private:
      

    int     fd_acc_x;               // 加速度X轴文件句柄
    int     fd_acc_y;               // 加速度Y轴文件句柄
    int     fd_acc_z;               // 加速度Z轴文件句柄
    int     fd_gyro_x;              // 角速度X轴文件句柄
    int     fd_gyro_y;              // 角速度Y轴文件句柄
    int     fd_gyro_z;              // 角速度Z轴文件句柄
    int     fd_mag_x;               // 磁力计X轴文件句柄
    int     fd_mag_y;               // 磁力计Y轴文件句柄
    int     fd_mag_z;               // 磁力计Z轴文件句柄

    // 私有化核心函数：通过文件句柄读取原始数据并转int16
    int16 imu_read_fd_data(int fd);
    // 私有化函数：关闭所有打开的文件句柄
    void imu_close_all_fd(void);

    // 禁用拷贝构造和赋值重载，规范约束
    zf_device_imu(const zf_device_imu&) = delete;
    zf_device_imu& operator=(const zf_device_imu&) = delete;

public:
    imu_device_type_enum   imu_type;       
//-------------------------------------------------------------------------------------------------------------------
// 函数简介 构造函数
// 参数说明 无
// 返回参数 无
// 使用示例 zf_device_imu imu_dev;
//-------------------------------------------------------------------------------------------------------------------
    zf_device_imu(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 析构函数
// 参数说明 无
// 返回参数 无
// 使用示例 自动调用
//-------------------------------------------------------------------------------------------------------------------
    ~zf_device_imu(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 获取当前识别到的IMU设备类型
// 参数说明 无
// 返回参数 imu_device_type_enum 设备类型枚举值
// 使用示例 imu_device_type_enum type = imu_dev.get_dev_type();
//-------------------------------------------------------------------------------------------------------------------
    imu_device_type_enum get_dev_type(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取加速度X轴原始数据
// 参数说明 无
// 返回参数 int16 加速度X轴原始值，未初始化返回0
// 使用示例 int16 ax = imu_dev.get_acc_x();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_acc_x(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取加速度Y轴原始数据
// 参数说明 无
// 返回参数 int16 加速度Y轴原始值，未初始化返回0
// 使用示例 int16 ay = imu_dev.get_acc_y();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_acc_y(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取加速度Z轴原始数据
// 参数说明 无
// 返回参数 int16 加速度Z轴原始值，未初始化返回0
// 使用示例 int16 az = imu_dev.get_acc_z();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_acc_z(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取角速度X轴原始数据
// 参数说明 无
// 返回参数 int16 角速度X轴原始值，未初始化返回0
// 使用示例 int16 gx = imu_dev.get_gyro_x();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_gyro_x(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取角速度Y轴原始数据
// 参数说明 无
// 返回参数 int16 角速度Y轴原始值，未初始化返回0
// 使用示例 int16 gy = imu_dev.get_gyro_y();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_gyro_y(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取角速度Z轴原始数据
// 参数说明 无
// 返回参数 int16 角速度Z轴原始值，未初始化返回0
// 使用示例 int16 gz = imu_dev.get_gyro_z();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_gyro_z(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取磁力计X轴原始数据
// 参数说明 无
// 返回参数 int16 磁力计X轴原始值，660系列返回0，未初始化返回0
// 使用示例 int16 mx = imu_dev.get_mag_x();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_mag_x(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取磁力计Y轴原始数据
// 参数说明 无
// 返回参数 int16 磁力计Y轴原始值，660系列返回0，未初始化返回0
// 使用示例 int16 my = imu_dev.get_mag_y();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_mag_y(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 读取磁力计Z轴原始数据
// 参数说明 无
// 返回参数 int16 磁力计Z轴原始值，660系列返回0，未初始化返回0
// 使用示例 int16 mz = imu_dev.get_mag_z();
//-------------------------------------------------------------------------------------------------------------------
    int16 get_mag_z(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 IMU设备初始化【核心流程：写1初始化→读该文件获型号→按型号开文件】
// 参数说明 无
// 返回参数 imu_device_type_enum 识别到的IMU设备类型，值对应枚举体
// 使用示例 imu_device_type_enum type = imu_dev.init();
//-------------------------------------------------------------------------------------------------------------------
    imu_device_type_enum init(void);
    
};

#endif