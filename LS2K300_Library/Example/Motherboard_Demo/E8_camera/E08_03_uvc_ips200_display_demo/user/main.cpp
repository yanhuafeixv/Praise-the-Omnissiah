zf_device_ips200 ips200;        // 定义2寸IPS SPI屏幕的设备对象，用于屏幕的所有操作
zf_device_uvc uvc_dev;          // 定义UVC免驱摄像头的设备对象，用于摄像头的所有操作
uint16 count = 0;               // 帧计数变量，用于控制图像显示模式切换的频率
uint8  show_flag = 0;           // 显示模式切换标志位：0-显示灰度图像，1-显示RGB彩色图像

/**
 * @brief 程序主函数，程序入口
 * @return int 程序退出状态码，正常退出返回0，异常返回-1
 */
int main() 
{
    // 初始化2寸IPS屏幕，传入帧缓冲设备路径，完成屏幕底层驱动、显示区域初始化
    ips200.init(FB_PATH);
    
    // 初始化UVC免驱摄像头，传入USB摄像头设备路径
    // 初始化返回值小于0表示初始化失败，直接退出程序
    if(uvc_dev.init(UVC_PATH) < 0)
    {
        return -1;
    }

    // 程序主循环，死循环持续运行
    while(1)
    {
        // 阻塞等待摄像头完成新一帧图像的采集和刷新
        // 返回值等于0表示成功获取到新的一帧图像数据
        if(uvc_dev.wait_image_refresh() == 0)
        {
            // 帧计数器累加，达到200帧时执行模式切换
            if(count++ >= 200)
            {
                show_flag = !show_flag;  // 取反切换显示模式：灰度<->彩色
                count = 0;               // 计数器清零，重新计数
            }

            // 根据标志位，交替显示 灰度图像 / RGB彩色图像
            // 逻辑：显示200帧灰度图像 → 切换显示200帧彩色图像 → 循环往复
            if(show_flag) // 标志位为1，显示RGB彩色图像
            {
                // 获取摄像头采集到的RGB图像数据缓冲区首地址指针
                // RGB图像数据格式为uint16位宽（565格式：5位红+6位绿+5位蓝）
                uint16* rgb_image = uvc_dev.get_rgb_image_ptr();
                
                // 判空：确保图像指针有效，避免空指针操作导致程序崩溃
                if(NULL != rgb_image)
                {
                    // 在屏幕的(0,0)坐标起点，显示RGB图像
                    // 显示尺寸为摄像头采集的标准宽高 UVC_WIDTH * UVC_HEIGHT
                    ips200.displayimage_rgb565(rgb_image, UVC_WIDTH, UVC_HEIGHT);
                }
                continue; // 跳过后续灰度显示逻辑，直接进入下一次循环
            }
            else // 标志位为0，显示灰度图像
            {
                // 获取摄像头采集到的灰度图像数据缓冲区首地址指针
                // 灰度图像数据格式为uint8位宽（0-255表示亮度等级，0最暗，255最亮）
                uint8* gray_image = uvc_dev.get_gray_image_ptr();
                
                // 判空：确保图像指针有效
                if(NULL != gray_image)
                {
                    // 在屏幕的(0,0)坐标起点，显示灰度图像
                    // 显示尺寸为摄像头采集的标准宽高 UVC_WIDTH * UVC_HEIGHT
                    ips200.displayimage_gray(gray_image, UVC_WIDTH, UVC_HEIGHT);
                }
            }
        }
        // 短延时10毫秒，降低CPU占用率，同时防止循环执行过快导致的资源抢占问题
        system_delay_ms(10);
    }
    return 0; // 理论上不会执行到这里，主循环为死循环
}
