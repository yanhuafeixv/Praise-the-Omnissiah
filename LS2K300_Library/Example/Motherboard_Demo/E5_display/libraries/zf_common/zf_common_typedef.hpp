#ifndef _zf_common_typedef_h_
#define _zf_common_typedef_h_

//====================================================================================================================
// 基础标准库头文件 【标准C库 基础依赖】
//====================================================================================================================
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>
#include <stdbool.h>
#include <stdarg.h>
#include <unistd.h>
#include <errno.h>

//====================================================================================================================
// Linux系统调用头文件 【系统IO、内存映射、设备控制】
//====================================================================================================================
#include <fcntl.h>
#include <sys/mman.h>
#include <sys/ioctl.h>
#include <unistd.h>

//====================================================================================================================
// Linux 进程/线程/调度 头文件 【线程、实时调度、定时器依赖】
//====================================================================================================================
#include <pthread.h>
#include <sched.h>
#include <sys/prctl.h>
#include <signal.h>

//====================================================================================================================
// Linux 高精度定时器/时间相关头文件 【timerfd定时器、时间戳、时钟】
//====================================================================================================================
#include <sys/timerfd.h>
#include <sys/time.h>
#include <time.h>

//====================================================================================================================
// Linux IO多路复用头文件 【epoll事件监听 定时器必备】
//====================================================================================================================
#include <sys/epoll.h>

//====================================================================================================================
// Linux 帧缓冲设备头文件 【LCD/屏幕驱动 fb0设备依赖】
//====================================================================================================================
#include <linux/fb.h>

//====================================================================================================================
// Linux 网络相关头文件 【TCP/UDP 网络通信】
//====================================================================================================================
#include <sys/socket.h>
#include <netinet/in.h>
#include <arpa/inet.h>

//====================================================================================================================
// C++ 标准库头文件 【兼容C++编译、函数对象、原子操作、chrono时间】
//====================================================================================================================
#include <iostream>
#include <functional>
#include <atomic>
#include <chrono>
#include <thread>
#include <cstring>
#include <cstdlib>
#include <cerrno>

//====================================================================================================================
// 格式化输出相关 【uint64打印专用】
//====================================================================================================================
#include <inttypes.h>

//=================================================== 数据类型定义 ===================================================
// 尽量使用 stdint.h 定义的类型名称 避免冲突 此处为兼容逐飞库传统定义
// 注：uint64/int64 注释保留原格式，如需启用直接解除注释即可
//=================================================== 数据类型定义 ===================================================
typedef unsigned char       uint8;                                              // 无符号  8 bits
typedef unsigned short int  uint16;                                             // 无符号 16 bits
typedef unsigned int        uint32;                                             // 无符号 32 bits
// typedef unsigned long long  uint64;                                             // 无符号 64 bits

typedef signed char         int8;                                               // 有符号  8 bits
typedef signed short int    int16;                                              // 有符号 16 bits
typedef signed int          int32;                                              // 有符号 32 bits
// typedef signed long long    int64;                                              // 有符号 64 bits

typedef volatile uint8      vuint8;                                             // 易变性修饰 无符号  8 bits
typedef volatile uint16     vuint16;                                            // 易变性修饰 无符号 16 bits
typedef volatile uint32     vuint32;                                            // 易变性修饰 无符号 32 bits
// typedef volatile uint64     vuint64;                                            // 易变性修饰 无符号 64 bits

typedef volatile int8       vint8;                                              // 易变性修饰 有符号  8 bits
typedef volatile int16      vint16;                                             // 易变性修饰 有符号 16 bits
typedef volatile int32      vint32;                                             // 易变性修饰 有符号 32 bits
// typedef volatile int64      vint64;                                             // 易变性修饰 有符号 64 bits

//=================================================== 宏定义 ==========================================================
#define ZF_WEAK             __attribute__((weak))                                // 弱定义 兼容库函数重写

#endif