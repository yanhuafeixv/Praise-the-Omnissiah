#ifndef __ZF_DEVICE_TFT180_FB_HPP__
#define __ZF_DEVICE_TFT180_FB_HPP__

#include "zf_common_typedef.hpp"
#include "zf_device_uvc.hpp"

#define DEFAULT_PENCOLOR         (RGB565_RED)                          // 默认的画笔颜色
#define DEFAULT_BGCOLOR          (RGB565_WHITE)                        // 默认的背景颜色

#define FB_PATH                 "/dev/fb0"                             // framebuffer设备节点路径

//-------------------------------------------------------------------------------------------------------------------
// 类名         zf_device_tft180
// 说明         IPS200显示屏 framebuffer驱动C++封装类，128*160分辨率 RGB565格式
// 适用         LS2K0300平台，所有成员函数无tft180前缀，封装性强，调用简洁
//-------------------------------------------------------------------------------------------------------------------
class zf_device_tft180
{
private:
    uint16 pen_color;                          // 画笔颜色
    uint16 bg_color;                           // 背景颜色
    int width;                                 // 屏幕宽度
    int height;                                // 屏幕高度
    unsigned short *screen_base;               // 映射后的显存基地址

public:
    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     无参构造函数
    // 参数说明     void
    // 返回参数     void
    // 使用示例     zf_device_tft180 tft180;
    // 备注信息     初始化默认画笔和背景颜色，显存地址置空
    //-------------------------------------------------------------------------------------------------------------------
    zf_device_tft180(void);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     清屏函数
    // 参数说明     void
    // 返回参数     void
    // 使用示例     tft180.clear();
    // 备注信息     将屏幕清空成背景颜色
    //-------------------------------------------------------------------------------------------------------------------
    void clear(void);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     屏幕填充函数
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     tft180.full(RGB565_BLACK);
    // 备注信息     将屏幕填充成指定颜色
    //-------------------------------------------------------------------------------------------------------------------
    void full(const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     画点函数
    // 参数说明     x               坐标x方向的点位置 [0, width-1]
    // 参数说明     y               坐标y方向的点位置 [0, height-1]
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     tft180.draw_point(10, 20, RGB565_BLUE);
    // 备注信息     在指定坐标绘制一个像素点
    //-------------------------------------------------------------------------------------------------------------------
    void draw_point(uint16 x, uint16 y, const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     画线函数
    // 参数说明     x_start         坐标x方向的起点 [0, width-1]
    // 参数说明     y_start         坐标y方向的起点 [0, height-1]
    // 参数说明     x_end           坐标x方向的终点 [0, width-1]
    // 参数说明     y_end           坐标y方向的终点 [0, height-1]
    // 参数说明     color           颜色格式 RGB565 或者可以使用 zf_common_font.h 内 rgb565_color_enum 枚举值或者自行写入
    // 返回参数     void
    // 使用示例     tft180.draw_line(0, 0, 10, 10, RGB565_RED);
    // 备注信息     在指定两点之间绘制一条直线
    //-------------------------------------------------------------------------------------------------------------------
    void draw_line(uint16 x_start, uint16 y_start, uint16 x_end, uint16 y_end, const uint16 color);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     TFT180 显示波形
    // 参数说明     x               坐标x方向的起点 参数范围 [0, width-1]
    // 参数说明     y               坐标y方向的起点 参数范围 [0, height-1]
    // 参数说明     *wave           波形数组指针
    // 参数说明     width           波形实际宽度
    // 参数说明     value_max       波形实际最大值
    // 参数说明     dis_width       波形显示宽度 参数范围 [0, width]
    // 参数说明     dis_value_max   波形显示最大值 参数范围 [0, height]
    // 返回参数     void
    // 使用示例     tft180.show_wave(0, 0, data, 128, 64, 64, 32);
    // 备注信息     
    //-------------------------------------------------------------------------------------------------------------------
    void show_wave (uint16 x, uint16 y, const uint16 *wave, uint16 width, uint16 value_max, uint16 dis_width, uint16 dis_value_max);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示单个字符
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的单个字符 数据类型 char
    // 返回参数     void
    // 使用示例     tft180.show_char(10, 20, 'A');
    // 备注信息     显示8x16的ASCII字符集，字符范围ASCII码32~127
    //-------------------------------------------------------------------------------------------------------------------
    void show_char(uint16 x, uint16 y, const char dat);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示字符串
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的字符串首地址 数据类型 const char[]
    // 返回参数     void
    // 使用示例     tft180.show_string(10, 20, "SEEKFREE");
    // 备注信息     连续显示8x16的ASCII字符，字符串以'\0'结尾
    //-------------------------------------------------------------------------------------------------------------------
    void show_string(uint16 x, uint16 y, const char dat[]);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示有符号整型数
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 int32
    // 参数说明     num             整数显示长度 最高支持10位
    // 返回参数     void
    // 使用示例     tft180.show_int(10, 20, -123, 3);
    // 备注信息     超出长度会显示低位数值，不足长度补空格，支持负数显示负号
    //-------------------------------------------------------------------------------------------------------------------
    void show_int(uint16 x, uint16 y, const int32 dat, uint8 num);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示无符号整型数
    // 参数说明     x               坐标x方向的起点 [0, width-1]
    // 参数说明     y               坐标y方向的起点 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 uint32
    // 参数说明     num             整数显示长度 最高支持10位
    // 返回参数     void
    // 使用示例     tft180.show_uint(10, 20, 123, 3);
    // 备注信息     超出长度会显示低位数值，不足长度补空格，无符号不显示负号
    //-------------------------------------------------------------------------------------------------------------------
    void show_uint(uint16 x, uint16 y, const uint32 dat, uint8 num);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示浮点数(去除整数部分无效的0)
    // 参数说明     x               坐标x方向的起点 参数范围 [0, width-1]
    // 参数说明     y               坐标y方向的起点 参数范围 [0, height-1]
    // 参数说明     dat             需要显示的变量 数据类型 double
    // 参数说明     num             整数位显示长度   最高8位
    // 参数说明     pointnum        小数位显示长度   最高6位
    // 返回参数     void
    // 使用示例     tft180.show_float(0, 0, x, 2, 3);
    // 备注信息     特别注意当发现小数部分显示的值与你写入的值不一样的时候，可能是由于浮点数精度丢失问题导致的，这并不是显示函数的问题，有关问题的详情，请自行百度学习   浮点数精度丢失问题。负数会显示一个 '-'号
    //-------------------------------------------------------------------------------------------------------------------
    void show_float(uint16 x, uint16 y, const double dat, uint8 num, uint8 pointnum);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     TFT180 显示 8bit 灰度图像 带二值化阈值
    // 参数说明     x               坐标x方向的起点 参数范围 [0, width-1]
    // 参数说明     y               坐标y方向的起点 参数范围 [0, height-1]
    // 参数说明     *image          图像数组指针
    // 参数说明     width           图像实际宽度
    // 参数说明     height          图像实际高度
    // 参数说明     dis_width       图像显示宽度 参数范围 [0, width]
    // 参数说明     dis_height      图像显示高度 参数范围 [0, height]
    // 参数说明     threshold       二值化显示阈值 0-不开启二值化
    // 返回参数     void
    // 使用示例     tft180.show_gray_image(0, 0, image, UVC_WIDTH, UVC_HEIGHT, UVC_WIDTH, UVC_HEIGHT, 0);
    // 备注信息     用于显示总钻风的图像
    //              如果要显示二值化图像 直接修改最后一个参数为需要的二值化阈值即可
    //              如果要显示二值化图像 直接修改最后一个参数为需要的二值化阈值即可
    //              如果要显示二值化图像 直接修改最后一个参数为需要的二值化阈值即可
    //-------------------------------------------------------------------------------------------------------------------
    void show_gray_image (uint16 x, uint16 y, const uint8 *image, uint16 width, uint16 height, uint16 dis_width, uint16 dis_height, uint8 threshold);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     TFT180 显示 RGB565 彩色图像
    // 参数说明     x               坐标x方向的起点 参数范围 [0, width-1]
    // 参数说明     y               坐标y方向的起点 参数范围 [0, height-1]
    // 参数说明     *image          图像数组指针
    // 参数说明     width           图像实际宽度
    // 参数说明     height          图像实际高度
    // 参数说明     dis_width       图像显示宽度 参数范围 [0, width]
    // 参数说明     dis_height      图像显示高度 参数范围 [0, height]
    // 参数说明     color_mode      色彩模式 0-低位在前 1-高位在前
    // 返回参数     void
    // 使用示例     tft180.show_rgb565_image(0, 0, image, UVC_WIDTH, UVC_HEIGHT, UVC_WIDTH, UVC_HEIGHT, 1);
    // 备注信息     用于显示凌瞳的 RGB565 的图像
    //              如果要显示低位在前的其他 RGB565 图像 修改最后一个参数即可
    //              如果要显示低位在前的其他 RGB565 图像 修改最后一个参数即可
    //              如果要显示低位在前的其他 RGB565 图像 修改最后一个参数即可
    //-------------------------------------------------------------------------------------------------------------------
    void show_rgb565_image (uint16 x, uint16 y, const uint16 *image, uint16 width, uint16 height, uint16 dis_width, uint16 dis_height, uint8 color_mode);

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     TFT180 显示总钻风图像 不带二值化 显示灰度图像
    // 参数说明     p               图像数组
    // 参数说明     width           显示宽度
    // 参数说明     height          显示高度
    // 返回参数     void
    // 使用示例     tft180.displayimage_gray(mt9v03x_image[0], 94, 60);
    // 备注信息     如果要显示二值化图像就去调用 tft180.show_gray_image 函数
    //-------------------------------------------------------------------------------------------------------------------
    void displayimage_gray(const uint8 *p, uint16 width, uint16 height)        
    {
        this->show_gray_image(0, 0, (p), UVC_WIDTH, UVC_HEIGHT, (width), (height), 0);
    }

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     TFT180 显示彩色图像
    // 参数说明     p               图像数组
    // 参数说明     width           显示宽度
    // 参数说明     height          显示高度
    // 返回参数     void
    // 使用示例     tft180.displayimage_rgb565(scc8660_image[0], 80, 60);
    // 备注信息     
    //-------------------------------------------------------------------------------------------------------------------
    void displayimage_rgb565(const uint16 *p, uint16 width, uint16 height)       
    {
        this->show_rgb565_image(0, 0, (p), UVC_WIDTH, UVC_HEIGHT, (width), (height), 0);
    }

    //-------------------------------------------------------------------------------------------------------------------
    // 函数简介     显示屏初始化函数
    // 参数说明     path            framebuffer设备节点路径 如 "/dev/fb0"
    // 返回参数     void
    // 使用示例     tft180.init("/dev/fb0");
    // 备注信息     打开fb设备、获取屏幕参数、映射显存、初始化清屏，程序初始化阶段只调用一次
    //-------------------------------------------------------------------------------------------------------------------
    void init(const char *path = FB_PATH, uint8 is_reload_driver = 1);
};

#endif